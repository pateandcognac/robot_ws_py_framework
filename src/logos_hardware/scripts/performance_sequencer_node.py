#!/usr/bin/env python3
"""
Performance Sequencer: the single playback clock for Logos's Text-to-
Performance pipeline (TTP v3).

Merges the old audio_and_face_playback.py + arm_playback_node.py into one
node so every modality (speech audio, face, arms, future tracks) is
sequenced off the same cue queue instead of guessing at each other's
timing.

TTP v3 provisional cue timeline: TTS cues enter the queue at *announce*
time (/performance/cue_announce, published by the director before any
synthesis) carrying est_duration as a provisional clock. Audio
(/face/tts_chunk SpeechData) attaches to its pending cue whenever it
lands and becomes the master clock the moment it exists. While a cue is
next to play but its audio is still synthesizing, the sequencer stages
the first face/arm frame -- a held pose, an actor hitting the mark -- and
nothing else animates until speech actually starts. Audio later than
~audio_wait_s plays out silently on est_duration rather than wedging the
queue. /performance/cue_playing fires the moment a cue's audio (or silent
playout) actually starts -- that's the caption/choreography sync point.

Cues arrive as:
- /performance/cue_announce (String JSON): TTS cue skeletons, pre-audio.
- /face/tts_chunk (SpeechData): the audio for an announced cue (also the
  legacy full-cue path for publishers that never announce).
- /face/emoji_command (String JSON): silent face cues, {"emoji": ...,
  "duration": ..., "text": ...}.
- /arm/emoji_command (String JSON): arm-only cues, same payload.

Animation per channel (face and arms share one playback helper):
1. A track delivered on /performance/face_track // /performance/arm_track
   (String JSON) by the animator nodes -- generated by the tiny models,
   their saved stores, or the animator's own LUT fallback. Payload:
   {"cue_id", "source", "status": "partial|complete|failed|lut",
   "frames": [sparse semantic frames]}. Frames may stream in mid-cue;
   pacing re-stretches over the remaining cue time every time new
   information arrives (frames or a terminal status).
2. The sequencer's own semantic LUT copy, when the animator answers
   status="lut" (pure-emoji fast path), the cascade failed, the cue has
   no cue_id (legacy commands), or no track message shows up at all by
   half the cue (animator down -- the LUT fallback must survive that).
3. Nothing (audio still plays; the last pose holds).

The v2 LUT "cold-open with mid-cue track switch" is gone (Mark's call,
2026-07-06): live generation lands in 1-2s, so the sequencer plays
whatever single track the cascade resolves instead of second-guessing
sources mid-cue.
"""

